"use strict";

const randomNumber = Math.round(Math.random() * 4 + 1);

if (randomNumber === 1) {
    console.log("1");
} else if (randomNumber === 2) {
    console.log("2");
} else if (randomNumber === 3) {
    console.log("3");
} else if (randomNumber === 4) {
    console.log("4");
} else if (randomNumber === 5) {
    console.log("5");
}